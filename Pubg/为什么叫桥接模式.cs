using System;

namespace WeaponLib
{
    /// <summary>
    /// 为什么叫"桥接"模式？Bridge Pattern 命名由来详解
    /// </summary>
    public class WhyCalledBridge
    {
        /*
         * ====================================================================
         * 为什么叫"桥接"模式？
         * ====================================================================
         * 
         * 想象一座桥梁🌉：
         * 
         *     陆地A                桥梁                陆地B
         *   (抽象层)            (连接器)             (实现层)
         *      │                   │                   │
         *      │                   │                   │
         *    Weapon ═══════════════════════════ IShootingMode
         *      │              聚合关系                 │
         *      │                                       │
         *   ┌──┴──┐                                ┌───┴────┐
         *   │     │                                │   │    │
         * Rifle Pistol                         Single Burst FullAuto
         * 
         * 
         * 桥梁的作用：
         * ✓ 连接两岸（连接抽象和实现）
         * ✓ 两岸可以独立发展（互不影响）
         * ✓ 保持交通畅通（可以动态组合）
         * 
         * 
         * ====================================================================
         * 关键理解：两个维度的独立变化
         * ====================================================================
         * 
         *                      没有桥的情况（直接继承）
         *                  ═════════════════════════════
         * 
         *                          Weapon
         *                            |
         *            ┌───────────────┼───────────────┐
         *            |               |               |
         *      AK47Single      AK47Burst      AK47FullAuto
         *      M16Single       M16Burst       M16FullAuto
         *      M1911Single     M1911Burst     M1911FullAuto
         *      ...
         * 
         * 问题：类爆炸！3种武器 × 3种模式 = 9个类，而且难以扩展
         * 
         * 
         *                      有桥的情况（桥接模式）
         *                  ═════════════════════════════
         * 
         *    维度1: 武器类型                维度2: 射击模式
         *    ═══════════════               ═══════════════
         *        Weapon                    IShootingMode
         *          |                             |
         *      ┌───┼───┐                    ┌────┼────┐
         *      |   |   |                    |    |    |
         *    Rifle Pistol                 Single Burst Full
         *      M16  M1911
         * 
         *    通过"桥"连接：Weapon.ShootingMode
         * 
         * 优势：3个武器类 + 3个模式类 = 6个类
         *      可以自由组合：Rifle + Single, Pistol + Burst, ...
         * 
         * 
         * ====================================================================
         * "桥"在哪里？
         * ====================================================================
         */

        public class BridgeLocation
        {
            /*
             * "桥"就是这个引用！
             * ════════════════════
             */
            public class Weapon
            {
                // ⚠️ 这就是"桥"！连接两个独立维度的纽带
                public IShootingMode ShootingMode { get; set; }
                //     ↑                ↑
                //   左岸持有          右岸的引用
                
                // 构造函数：搭建桥梁
                public Weapon(string name, IShootingMode mode)
                {
                    ShootingMode = mode;  // 桥梁连接
                }
                
                // 使用桥梁：走过桥去调用对岸的方法
                public void Fire()
                {
                    ShootingMode.Shoot();  // 通过桥调用
                    //    ↑      ↑
                    //  走过桥   到达对岸
                }
                
                // 重建桥梁：可以切换到新的对岸
                public void ChangeShootingMode(IShootingMode newMode)
                {
                    ShootingMode = newMode;  // 拆旧桥，建新桥
                }
            }
        }

        /*
         * ====================================================================
         * 现实中的桥梁类比
         * ====================================================================
         * 
         * 想象上海的黄浦江两岸：
         * 
         * 浦西（左岸）           外白渡桥           浦东（右岸）
         * ┌─────────┐         ══════════         ┌─────────┐
         * │ 南京路  │────────────桥────────────────│ 陆家嘴  │
         * │ 淮海路  │                              │ 世纪大道│
         * │ 人民广场│                              │ 东方明珠│
         * └─────────┘                              └─────────┘
         * 
         * 桥的作用：
         * 1. 连接功能：让两岸可以沟通
         * 2. 独立性：浦西盖楼不影响浦东，浦东发展不影响浦西
         * 3. 灵活性：可以建多座桥，可以拆了重建
         * 
         * 对应到代码：
         * 浦西 = Weapon（武器抽象）
         * 浦东 = IShootingMode（射击模式实现）
         * 桥 = ShootingMode属性（聚合关系）
         * 
         * 建桥 = new Rifle("AK-47", fullAutoMode)
         * 过桥 = weapon.Fire() 内部调用 shootingMode.Shoot()
         * 换桥 = ChangeShootingMode(newMode)
         * 
         * 
         * ====================================================================
         * 为什么不叫"连接"模式或"分离"模式？
         * ====================================================================
         * 
         * "桥接"这个词很精妙，因为它强调：
         * 
         * 1. Bridge（桥）是一个中性连接器
         *    - 不是"融合"（太紧密）
         *    - 不是"分离"（太松散）
         *    - 而是"桥接"（恰到好处的连接）
         * 
         * 2. 桥的特点完美对应模式特点：
         *    ✓ 连接两岸 → 连接抽象和实现
         *    ✓ 两岸独立 → 两个维度独立变化
         *    ✓ 可以重建 → 可以动态替换实现
         *    ✓ 交通要道 → 频繁调用的关键路径
         * 
         * 3. 形象化记忆：
         *    一看到"桥接"，就想到"连接两个独立的东西"
         * 
         * 
         * ====================================================================
         * 对比其他可能的命名（假设）
         * ====================================================================
         * 
         * ✗ "分离模式"（Separation Pattern）
         *    - 只强调了分离，没体现连接
         *    - 听起来像是把东西拆开，不够形象
         * 
         * ✗ "双维度模式"（Two-Dimension Pattern）
         *    - 太技术化，不够生动
         *    - 没有体现连接的方式
         * 
         * ✗ "组合模式"（Combination Pattern）
         *    - 容易和另一个设计模式冲突
         *    - 没体现独立性
         * 
         * ✓ "桥接模式"（Bridge Pattern）
         *    - 形象生动，容易理解
         *    - 体现了连接和独立的双重性质
         *    - 一听就知道是连接两个东西
         * 
         * 
         * ====================================================================
         * 图解：有桥 vs 没桥
         * ====================================================================
         * 
         * 没有桥（类爆炸）：
         * ════════════════
         * 
         * 如果每种组合都是一个类：
         * 
         * AK47SingleShot ─┐
         * AK47Burst      ─┤
         * AK47FullAuto   ─┤
         * M16SingleShot  ─┤
         * M16Burst       ─┤── 9个类！维护噩梦
         * M16FullAuto    ─┤
         * M1911SingleShot─┤
         * M1911Burst     ─┤
         * M1911FullAuto  ─┘
         * 
         * 添加一种新武器？要写3个类
         * 添加一种新模式？要改所有武器，写N个类
         * 
         * 
         * 有桥（桥接模式）：
         * ════════════════
         * 
         * 武器侧（3个类）        射击模式侧（3个类）
         * ┌────────┐            ┌──────────┐
         * │ Rifle  │            │  Single  │
         * │ Pistol │   ═桥═     │  Burst   │
         * │ Shotgun│            │ FullAuto │
         * └────────┘            └──────────┘
         * 
         * 自由组合：
         * Rifle + Single    ✓
         * Rifle + Burst     ✓
         * Pistol + FullAuto ✓
         * ... 任意组合
         * 
         * 添加新武器？写1个类，继承Weapon
         * 添加新模式？写1个类，实现IShootingMode
         * 零修改现有代码！
         * 
         * 
         * ====================================================================
         * 再用一个生活例子：遥控器与电器
         * ====================================================================
         * 
         * 遥控器（抽象）          桥接          电器（实现）
         * ┌──────────┐         ═══════        ┌──────┐
         * │ 通用遥控器│ ───────桥─────────────> │ 电视 │
         * │ 智能遥控器│                         │ 空调 │
         * └──────────┘                         │ 音响 │
         *                                      └──────┘
         * 
         * 桥 = 遥控器内的设备引用/配对
         * 
         * 好处：
         * - 遥控器类型可以独立增加（语音遥控器、手势遥控器）
         * - 电器类型可以独立增加（投影仪、加湿器）
         * - 一个遥控器可以切换控制不同电器（桥可以换）
         * 
         * 如果没有桥接模式：
         * TVRemote, ACRemote, StereoRemote（3种遥控器）
         * SmartTVRemote, SmartACRemote, SmartStereoRemote（又3个）
         * ... 组合爆炸
         * 
         * 
         * ====================================================================
         * 终极理解："桥"的本质
         * ====================================================================
         * 
         * 桥接模式的"桥"，本质上是：
         * 
         * 1. 物理意义的桥：
         *    - 一个对象引用（ShootingMode属性）
         *    - 通过这个引用调用另一边的方法
         * 
         * 2. 架构意义的桥：
         *    - 连接两个独立变化的维度
         *    - 让它们可以协同工作
         * 
         * 3. 设计意义的桥：
         *    - 分离关注点（Separation of Concerns）
         *    - 组合优于继承（Composition over Inheritance）
         * 
         * 
         * 一句话总结：
         * ═══════════════════════════════════════════════════════════
         * "桥接"这个名字，形象地表达了"用一个灵活的连接器，
         *  将两个可以独立演化的维度连接起来"这个核心思想。
         * ═══════════════════════════════════════════════════════════
         * 
         * 
         * ====================================================================
         * BridgeKit 中的"桥"在哪？
         * ====================================================================
         * 
         * public abstract class Weapon
         * {
         *     // ⚠️ 这就是桥！！！
         *     public IShootingMode ShootingMode { get; set; }
         *     //     │              └─────────────┐
         *     //     │                            │
         *     //   左岸抽象                      右岸实现
         *     //   (Weapon)                  (IShootingMode)
         *     //     │                            │
         *     //     ├─ Rifle                     ├─ SingleShotMode
         *     //     ├─ Pistol                    ├─ BrustMode
         *     //     └─ Shotgun                   └─ FullAutoMode
         *     
         *     public void Fire()
         *     {
         *         // 走过桥，调用对岸的方法
         *         ShootingMode.Shoot();  // ← 这是过桥的动作
         *     }
         *     
         *     public void ChangeShootingMode(IShootingMode newMode)
         *     {
         *         // 拆掉旧桥，建新桥
         *         ShootingMode = newMode;  // ← 这是换桥的动作
         *     }
         * }
         * 
         * 
         * 使用示例：
         * ═══════════
         * 
         * // 建桥：把AK-47和全自动模式连接起来
         * Weapon ak47 = new Rifle("AK-47", fullAutoMode);
         *                                   └─────┬─────┘
         *                                      搭建桥梁
         * 
         * // 过桥：通过桥调用射击模式的方法
         * ak47.Fire();  // 内部走过桥 → fullAutoMode.Shoot()
         * 
         * // 换桥：改变射击模式
         * ak47.ChangeShootingMode(brustMode);  // 拆旧桥建新桥
         * 
         * 
         * ====================================================================
         * 记忆口诀
         * ====================================================================
         * 
         * 两岸独立各发展，
         * 中间搭桥保联系。
         * 左岸抽象右实现，
         * 自由组合真灵活。
         * 
         * 桥在哪里？属性里，
         * 过桥通行靠调用。
         * 想换对岸？换个桥，
         * 这就是为啥叫桥接！
         * 
         */
    }
}
